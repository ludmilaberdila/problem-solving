public class PracticeVariables {
    public static void main(String[] args) {
        byte byte1 = 5, byte2 = 10, byte3 = 15;
        short short1 = 20, short2 = 25, short3 = 30;
        int int1 = 12, int2 = 22, int3 = 32;
        long long1 = 40, long2 = 45, long3 = 48;
        float float1 = 222.5f, float2 = 223.8f, float3 = 224.8f;
        double double1 = 98.9, double2 = 75.4, double3 = 66.9;
        boolean boolean1 = true, boolean2 = false;
        char char1 ='a', char2 = 'b', char3 = 'c';

        System.out.println("Bytes:" + byte1 + ","+ byte2 + "," + byte3);
        System.out.println("short:" + short1 + ","+ short2 + "," + short3);
        System.out.println("int:" + int1 + ","+ int2 + "," + int3);
        System.out.println("long:" + long1 + ","+ long2 + "," + long3);
        System.out.println("float:" + float1 + ","+ float2 + "," + float3);
        System.out.println("double:" + double1 + ","+ double2 + "," + double3);
        System.out.println("boolean:" + boolean1 + ","+ boolean2);
        System.out.println("char:" + char1 + ","+ char2 + "," + char3);

    }
}